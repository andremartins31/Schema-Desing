    private void setInitializeConnection() {
        Map<String,String> connection = connectionController.getLastConnection();
        if(!connection.isEmpty()){
            jCB_connectiontype.getModel().setSelectedItem(connection.get("connectiontype"));
            jTF_hostname.setText(connection.get("hostname"));
            jTF_databasename.setText(connection.get("databasename"));
            jTF_portnumber.setText(connection.get("portnumber"));
            jTF_username.setText(connection.get("username"));
            jTF_password.setText(connection.get("password"));
        }
            
    }
    
    
