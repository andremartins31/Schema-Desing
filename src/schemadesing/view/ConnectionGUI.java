    private void jB_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_okActionPerformed
        String connectionType = jCB_connectiontype.getSelectedItem().toString();
        String hostname = jTF_hostname.getText();
        String databasename = jTF_databasename.getText();
        String portnumber = jTF_portnumber.getText();
        String username = jTF_username.getText();
        String password = jTF_password.getText();
        
        if(connectionController.saveConnection(connectionType, hostname, databasename, portnumber, username, password)){            
            JOptionPane.showMessageDialog(null, "Connection Sucess", "Connection", 1);
            dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Connection Erro", "Connection", 0);
        }
    }//GEN-LAST:event_jB_okActionPerformed

